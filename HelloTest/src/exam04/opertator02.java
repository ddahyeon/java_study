package exam04;

public class opertator02 {

	public static void main(String[] args) {
		int n = 20;
		int n2 = 10;
		
		n+=n2;
		System.out.println(n+"\t"+n2);
		n-=n2;
		System.out.println(n+"\t"+n2);
		n*=n2;
		System.out.println(n+"\t"+n2);
		n/=n2;
		System.out.println(n+"\t"+n2);
		n%=n2;
		System.out.println(n+"\t"+n2);
		
		

	}

}
