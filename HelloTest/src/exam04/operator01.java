package exam04;

public class operator01 {

	public static void main(String[] args) {
		int n = 10;
		int n2 = 3;
		
		//연산결과를 변수에 저장 가능 
		int sum= n+ n2;
		System.out.println(sum);
		
		System.out.println(n+n2);
		System.out.println(n-n2);
		System.out.println(n/n2);
		System.out.println(n*n2);
		System.out.println(n%n2);
		
		// 추가정리	
		System.out.println(10/3);
		System.out.println(10/3.0);

	}

}
